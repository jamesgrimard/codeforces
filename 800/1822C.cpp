#include <bits/stdc++.h>

using namespace std;

int main() {
  ios::sync_with_stdio(false);cin.tie(0);cout.tie();
  
  int tt; cin >> tt;
  while (tt--) {
    long long n; cin >> n;
    cout << 4 * n + ((n-2LL)*(n-2LL+1LL)) + n << '\n';
    
  }
  
  
  
  
  
  
  
	

  return 0;
}
